        if (Ufield.getText().equals(" ") || Pfield.getPassword().equals(" ")) {

            JOptionPane.showMessageDialog(null, "Please Enter Username and Password");
            return;
        }
        if (Pfield.getPassword().equals("admin1") || Ufield.getText().equals("admin")) {
            JOptionPane.showMessageDialog(null, "Login Succesfull");
            new login().setVisible(false);
            new adminpage().setVisible(true);
            new login().dispose();

        }
        if (Pfield.getPassword().equals("in123") || Ufield.getText().equals("ein1")) {
            JOptionPane.showMessageDialog(null, "Login Succesfull");

            new login().setVisible(false);
            new InEmp().setVisible(true);
            new login().dispose();

        }
        if (Pfield.getPassword().equals("out123") || Ufield.getText().equals("eout1")) {
            JOptionPane.showMessageDialog(null, "Login Succesfull");

            new login().setVisible(false);
            new login().dispose();
            new OutEmp().setVisible(true);

        }
        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/stock", "root", "root");
            Statement st = con.createStatement();
            String query = "select password from login where username = '" + Ufield.getText() + "'";
            ResultSet rs = st.executeQuery(query);

            String get_password = " ";
            while (rs.next()) {
                get_password = rs.getString(3);
            }
            if (get_password.equals(Pfield.getPassword())) {
                JOptionPane.showMessageDialog(null, "Login Successful");
            } else {
                JOptionPane.showMessageDialog(null, "Username and Password is Incorrect");
            }

        } catch (Exception aw) {

        }
